function carga = carga(Vn,Sn,X,fp)
    %% Se n√£o houver fp, tratar como carga resistiva.
    addpath("../utils/apoio")
    
    carga = struct( ...
        'Vn', Vn, ...
        'Sn', Sn, ...
        'fp', fp, ...
        'X', X, ...
        'I', (conj(convert_phasor(Sn,acosd(fp)))/(sqrt(3)*Vn)) ...
    );

    disp(struct2table(carga));
end

